/*
 Modules make it possible to import JavaScript files into your application.  Modules are imported
 using 'require' statements that give you a reference to the module.

  It is a good idea to list the modules that your application depends on in the package.json in the project root
 */
const logger = require('winston');
const MauticClient = require('../helpers/mautic');
const util = require('util');

const mautic = new MauticClient(process.env.MAUTIC_URL, process.env.MAUTIC_USERNAME, process.env.MAUTIC_PASSWORD);
/*
 Once you 'require' a module you can reference the things that it exports.  These are defined in module.exports.

 For a controller in a127 (which this is) you should export the functions referenced in your Swagger document by name.

 Either:
  - The HTTP Verb of the corresponding operation (get, put, post, delete, etc)
  - Or the operationId associated with the operation in your Swagger document

  In the starter/skeleton project the 'get' operation on the '/hello' path has an operationId named 'hello'.  Here,
  we specify that in the exports of this module that 'hello' maps to the function named 'hello'
 */
module.exports = {
  getPointTriggers: getPointTriggers
};

/*
  Functions in a127 controllers used for operations should take two parameters:

  Param 1: a handle to the request object
  Param 2: a handle to the response object
 */
function getPointTriggers(req, res) {
  mautic.fetchPointTriggers()
  .then(mauticResponse => {
      res.json({mauticResponse})
    }).catch(error => {
      res.json(error)
    });
}
